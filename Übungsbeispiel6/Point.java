class Point {

	private int pointx;
	private int pointy;

	/*
	 * Constructor for the Point
	 */
	public Point(int pointx, int pointy) {
		this.pointx = pointx;
		this.pointy = pointy;
	}

	/*
	 * Getter for X Coordinate for the Point
	 */
	public int getPointx() {
		return pointx;
	}

	/*
	 * Setter for X Coordinate for the Point
	 */
	public void setPointx(int pointx) {
		this.pointx = pointx;
	}

	/*
	 * Getter for Y Coordinate for the Point
	 */
	public int getPointy() {
		return pointy;
	}

	/*
	 * Setter for Y Coordinate for the Point
	 */
	public void setPointy(int pointy) {
		this.pointy = pointy;
	}

	/*
	 * Returns the minimum of 2 integers
	 */


}
